    private void SalvarDepartamentoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SalvarDepartamentoActionPerformed
        // TODO add your handling code here:
        String nomeDepartamento = NomeDepartamento.getText();
        String tamanhoDepartamento = TamanhoDepartamento.getText();
        String codigoDepartamento = CodigoDepartamento.getText();
        int tamanho = Integer.parseInt(tamanhoDepartamento);
        Controlador controlador = new Controlador();
        Departamento departamento = new Departamento(codigoDepartamento, nomeDepartamento, tamanho);
        controlador.adicionarDepartamento(departamento);
        this.dispose();
    }//GEN-LAST:event_SalvarDepartamentoActionPerformed

